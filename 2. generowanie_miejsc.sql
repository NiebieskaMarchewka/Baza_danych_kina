Create procedure generowanie_miejsc_seans_1 ()
begin
	declare seans int;
	declare kolumna int;
    declare rzad int;
    set rzad=1;
	while rzad<=10 do
		set kolumna=1;
			repeat
				Insert into Miejsce (Seans_ID, Rzad, Kolumna) values (1, rzad, kolumna);
				set kolumna=kolumna + 1;
            until kolumna>10
			end repeat;
		set rzad=rzad + 1;
	end while;
end; //

